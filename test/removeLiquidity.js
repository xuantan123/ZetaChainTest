        const allowance = await lpTokenContract.allowance(to, ROUTER_ADDRESS);
        console.log(`üîπ Router ƒë∆∞·ª£c approve: ${ethers.utils.formatUnits(allowance, 18)} LP`);

        if (allowance.lt(liquidity)) {
            console.log("‚è≥ ƒêang approve LP Token...");
            const approveTx = await lpTokenContract.approve(ROUTER_ADDRESS, ethers.constants.MaxUint256);
            await approveTx.wait();
            console.log("‚úÖ Approve th√†nh c√¥ng!");
        } else {
            console.log("‚úÖ Router ƒë√£ ƒë∆∞·ª£c approve ƒë·ªß LP Token.");
        }

