const ERC20_ABI = [
  "function balanceOf(address owner) view returns (uint256)",
  "function approve(address spender, uint256 amount) external returns (bool)",
  "function decimals() view returns (uint8)",
  "function symbol() view returns (string)"
];

async function main() {
  console.log("🚀 Starting ZetaFarm deposit and withdraw test");
  console.log("==============================================");
  
  const [signer] = await ethers.getSigners();
  console.log(`📝 Using account: ${signer.address}`);
  
  // Connect to contracts
  const ZetaFarm = new ethers.Contract(ZetaFarmAddress, ZetaFarmABI, signer);
  const lpToken = new ethers.Contract(lpTokenAddress, ERC20_ABI, signer);
  
  // Get LP token info
  const lpSymbol = await lpToken.symbol();
  const lpDecimals = await lpToken.decimals();
  console.log(`📊 LP Token: ${lpSymbol} (${lpTokenAddress})`);
  
  const pid = 0; // Pool ID
  const depositAmount = ethers.utils.parseUnits("0.001", lpDecimals);
  
  console.log("\n📋 INITIAL STATE");
  console.log("----------------");
  
  // Check LP token balance
  let userLpBalance = await lpToken.balanceOf(signer.address);
  console.log(`💰 LP Balance: ${ethers.utils.formatUnits(userLpBalance, lpDecimals)} ${lpSymbol}`);
  
  // Check if user has enough LP tokens
  if (userLpBalance.lt(depositAmount)) {
    console.log(`❌ Not enough LP tokens. Need at least ${ethers.utils.formatUnits(depositAmount, lpDecimals)} ${lpSymbol}`);
    return;
  }
  
  // Check if pool exists
  try {
    const poolInfo = await ZetaFarm.poolInfo(pid);
    console.log(`✅ Pool ${pid} exists with allocation points: ${poolInfo.allocPoint.toString()}`);
  } catch (error) {
    console.log(`⚠️ Pool ${pid} doesn't exist. Creating new pool...`);
    const tx = await ZetaFarm.add(1000, lpTokenAddress, true, false);
    await tx.wait();
    console.log(`✅ Pool ${pid} created successfully`);
  }
  
  // Check user's current stake
  let userInfo = await ZetaFarm.userInfo(pid, signer.address);
  console.log(`🔒 Current stake: ${ethers.utils.formatUnits(userInfo.amount, lpDecimals)} ${lpSymbol}`);
  
  // Check pending rewards
  let pendingRewards = await ZetaFarm.pendingCake(pid, signer.address);
  console.log(`🎁 Pending rewards: ${ethers.utils.formatUnits(pendingRewards, 18)}`);
  
  console.log("\n🔄 DEPOSIT OPERATION");
  console.log("-------------------");
  console.log(`🔄 Depositing ${ethers.utils.formatUnits(depositAmount, lpDecimals)} ${lpSymbol}...`);
  
  // Approve LP tokens
  console.log(`👍 Approving ${ethers.utils.formatUnits(depositAmount, lpDecimals)} ${lpSymbol}...`);
  let approveTx = await lpToken.approve(ZetaFarmAddress, depositAmount);
  await approveTx.wait();
  console.log(`✅ Approval successful`);
  
  // Deposit LP tokens
  try {
    const depositTx = await ZetaFarm.deposit(pid, depositAmount);
    const receipt = await depositTx.wait();
    console.log(`✅ Deposit successful! (Gas used: ${receipt.gasUsed.toString()})`);
    
    // Get transaction hash
    console.log(`🔗 Transaction: ${receipt.transactionHash}`);
  } catch (error) {
    console.error(`❌ Deposit failed: ${error.message}`);
    // Continue with the test even if deposit fails
  }
  
  console.log("\n📋 POST-DEPOSIT STATE");
  console.log("--------------------");
  
  // Check LP token balance after deposit
  userLpBalance = await lpToken.balanceOf(signer.address);
  console.log(`💰 LP Balance after deposit: ${ethers.utils.formatUnits(userLpBalance, lpDecimals)} ${lpSymbol}`);
  
  // Check user's stake after deposit
  userInfo = await ZetaFarm.userInfo(pid, signer.address);
  console.log(`🔒 Stake after deposit: ${ethers.utils.formatUnits(userInfo.amount, lpDecimals)} ${lpSymbol}`);
  
  // Wait for some time to accumulate rewards
  console.log("\n⏳ Waiting for 10 seconds to accumulate rewards...");
  await new Promise(resolve => setTimeout(resolve, 10000));
  
  // Update pools
  console.log(`🔄 Updating pools...`);
  try {
    const updateTx = await ZetaFarm.massUpdatePools();
    await updateTx.wait();
    console.log(`✅ Pools updated`);
  } catch (error) {
    console.log(`⚠️ Pool update failed: ${error.message}`);
  }
  
  // Check pending rewards after waiting
  pendingRewards = await ZetaFarm.pendingCake(pid, signer.address);
  console.log(`🎁 Pending rewards after waiting: ${ethers.utils.formatUnits(pendingRewards, 18)}`);
  
  console.log("\n🔄 WITHDRAW OPERATION");
  console.log("--------------------");
  
  // Calculate withdraw amount (half of the staked amount or the deposit amount, whichever is smaller)
  const withdrawAmount = userInfo.amount.div(2).lt(depositAmount) 
    ? userInfo.amount.div(2) 
    : depositAmount;
  
  console.log(`🔄 Withdrawing ${ethers.utils.formatUnits(withdrawAmount, lpDecimals)} ${lpSymbol}...`);
  
  // Withdraw LP tokens
  try {
    const withdrawTx = await ZetaFarm.withdraw(pid, withdrawAmount);
    const receipt = await withdrawTx.wait();
    console.log(`✅ Withdrawal successful! (Gas used: ${receipt.gasUsed.toString()})`);
    
    // Get transaction hash
    console.log(`🔗 Transaction: ${receipt.transactionHash}`);
  } catch (error) {
    console.error(`❌ Withdrawal failed: ${error.message}`);
    if (error.reason) console.error(`   Reason: ${error.reason}`);
  }
  
  console.log("\n📋 FINAL STATE");
  console.log("-------------");
  
  // Check LP token balance after withdrawal
  userLpBalance = await lpToken.balanceOf(signer.address);
  console.log(`💰 LP Balance after withdrawal: ${ethers.utils.formatUnits(userLpBalance, lpDecimals)} ${lpSymbol}`);
  
  // Check user's stake after withdrawal
  userInfo = await ZetaFarm.userInfo(pid, signer.address);
  console.log(`🔒 Stake after withdrawal: ${ethers.utils.formatUnits(userInfo.amount, lpDecimals)} ${lpSymbol}`);
  
  // Check pending rewards after withdrawal
  pendingRewards = await ZetaFarm.pendingCake(pid, signer.address);
  console.log(`🎁 Pending rewards after withdrawal: ${ethers.utils.formatUnits(pendingRewards, 18)}`);
  
  console.log("\n✅ Test completed!");
}

